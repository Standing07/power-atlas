export type TKey = keyof (typeof dict)['zh']

interface LangCtx {
  lang: Lang
  setLang: (l: Lang) => void
  t: (key: TKey, vars?: Record<string, string | number>) => string
  countryName: (iso2: string | null, fallback: string) => string
}

const Ctx = createContext<LangCtx | null>(null)

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = localStorage.getItem('lang')
    return saved === 'en' || saved === 'zh' ? saved : 'zh'
  })
  const setLang = useCallback((l: Lang) => {
    localStorage.setItem('lang', l)
    setLangState(l)
  }, [])

  const value = useMemo<LangCtx>(() => {
    const displayNames = new Intl.DisplayNames([lang === 'zh' ? 'zh-Hant' : 'en'], {
      type: 'region',
      fallback: 'none',
    })
    return {
      lang,
      setLang,
      t: (key, vars) => {
        let s: string = dict[lang][key] ?? dict.zh[key] ?? key
        if (vars) for (const [k, v] of Object.entries(vars)) s = s.replaceAll(`{${k}}`, String(v))
        return s
      },
      countryName: (iso2, fallback) => {
        if (!iso2) return fallback
        try {
          return displayNames.of(iso2) ?? fallback
        } catch {
          return fallback
        }
      },
    }
  }, [lang, setLang])

  return <Ctx.Provider value={value}>{children}</Ctx.Provider>
}

export function useLang(): LangCtx {
  const ctx = useContext(Ctx)
  if (!ctx) throw new Error('useLang must be used inside LanguageProvider')
  return ctx
}

/** 雙語物件（人工資料檔用）的取值 */
export function pick(lang: Lang, obj: { zh: string; en: string } | undefined): string {
  return obj ? obj[lang] : ''
}
